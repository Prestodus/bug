<header>How's it made?</header>

<span class="firstletter">T</span>o be able to really show my skills, I decided not to take a conventional route.<br />
I didn't want my portfolio to be made in some sort of pre-fabricated content management system (although I do encourage using them for projects), as for me, this doesn't show how well you can code. Neither did I want something overly graphical, as I don't want to be hired for my graphical skills, but rather for my development skills.

<h3>So I developed my own CMS.</h3>